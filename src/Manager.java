public class Manager extends Person {


  private String dob;
  private int starRating;
  private Team team;


  public String getDob() {
    return dob;
  }

  public void setDob(String dob) {
    this.dob = dob;
  }

  public int getStarRating() {
    return starRating;
  }

  public void setStarRating(int starRating) {
    this.starRating = starRating;
  }

  public Team getTeam() {
    return team;
  }

}
